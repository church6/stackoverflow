from colors import FOREGROUND_RED, FOREGROUND_GREEN, FOREGROUND_BLUE, FOREGROUND_YELLOW, ENDCOLOR, LINE


class Aclass(object):
    '''
    Description : Aclass
    '''

    def foo(self, x):
        '''
        Description :
        '''
        print(f"executing foo({self}, {x})")

    @classmethod
    def class_foo(cls, x):
        '''
        Description :
        '''
        print(f"executing class_foo({cls}, {x})")

    @staticmethod
    def static_foo(x):
        '''
        Description :
        '''
        print(f"executing static_foo({x})")


class Answer1:
    '''
    Description : Answer1
    '''

    @staticmethod
    def code1():
        '''
        Description : code1
        '''
        print('Answer1::code1')
        obj = Aclass()
        obj.foo(1)
        obj.class_foo(1)
        Aclass.class_foo(1)
        obj.static_foo(1)
        Aclass.static_foo('hi')

    @staticmethod
    def code2():
        '''
        Description : code2
        '''
        print('Answer1::code2')

    @staticmethod
    def code3():
        '''
        Description : code3
        '''
        print('Answer1::code3')

    @staticmethod
    def verify():
        '''
        Description : verify
        '''
        print(f'{FOREGROUND_RED}{LINE}{ENDCOLOR}')
        Answer1.code1()
        Answer1.code2()
        Answer1.code3()
        print(f'{FOREGROUND_RED}{LINE}{ENDCOLOR}')


